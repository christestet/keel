#!/usr/bin/env bash
# M8 performance fixture runner. This is intentionally dependency-free: it
# generates the public reference corpus shape, runs the current keelc binary,
# and optionally compares metrics against a checked-in baseline.
set -euo pipefail

cd "$(git rev-parse --show-toplevel)"

keelc="target/release/keelc"
work_dir="target/m8-reference-corpus"
baseline="tests/performance/m8-reference/baseline.tsv"
metrics="target/m8-reference-metrics.tsv"
mode="check"
enforce=false
known_gaps=()

usage() {
  cat <<'EOF'
usage: scripts/m8-benchmark.sh [options]

Options:
  --keelc PATH       keelc binary to benchmark (default: target/release/keelc)
  --work-dir DIR     generated corpus directory (default: target/m8-reference-corpus)
  --baseline PATH    TSV baseline file (default: tests/performance/m8-reference/baseline.tsv)
  --metrics PATH     TSV metrics output (default: target/m8-reference-metrics.tsv)
  --mode check|full  check runs `keelc check`; full also runs two `keelc build`s
  --enforce          fail when a metric exceeds its budget or baseline +5%
  --known-gap METRIC record and report METRIC, but never let --enforce fail on
                      it. Repeatable. Use for a budget that is honestly
                      unenforceable today and documented as such (see
                      docs/m8-status.md) rather than silently gating on it.
EOF
}

is_known_gap() {
  local metric="$1"
  local candidate
  for candidate in "${known_gaps[@]+"${known_gaps[@]}"}"; do
    [ "$candidate" = "$metric" ] && return 0
  done
  return 1
}

while [ "$#" -gt 0 ]; do
  case "$1" in
    --keelc)
      keelc="$2"
      shift 2
      ;;
    --work-dir)
      work_dir="$2"
      shift 2
      ;;
    --baseline)
      baseline="$2"
      shift 2
      ;;
    --metrics)
      metrics="$2"
      shift 2
      ;;
    --mode)
      mode="$2"
      shift 2
      ;;
    --enforce)
      enforce=true
      shift
      ;;
    --known-gap)
      known_gaps+=("$2")
      shift 2
      ;;
    -h|--help)
      usage
      exit 0
      ;;
    *)
      echo "unknown option: $1" >&2
      usage >&2
      exit 2
      ;;
  esac
done

if [ "$mode" != "check" ] && [ "$mode" != "full" ]; then
  echo "--mode must be check or full" >&2
  exit 2
fi

now_ms() {
  perl -MTime::HiRes=time -e 'printf "%.0f\n", time() * 1000'
}

generate_corpus() {
  local handlers="${M8_REFERENCE_HANDLERS:-7200}"
  local out="$work_dir/main.keel"
  mkdir -p "$work_dir"

  {
    echo "// Generated by scripts/m8-benchmark.sh. Do not edit in place."
    echo "use std.print"
    echo
    echo "enum ServiceError {"
    echo "    Invalid(message: String),"
    echo "    Missing(id: Int),"
    echo "}"
    echo

    for i in $(seq 1 "$handlers"); do
      n="$(printf "%04d" "$i")"
      echo "struct Request$n {"
      echo "    id: Int"
      echo "    name: String"
      echo "}"
      echo
      echo "struct Response$n {"
      echo "    id: Int"
      echo "    body: String"
      echo "}"
      echo
      echo "fn handler_$n(input: Request$n) -> Response$n {"
      echo "    let adjusted = input.id + $i"
      echo "    if adjusted > 1000000 {"
      echo "        Response$n{id: adjusted - $i, body: input.name}"
      echo "    } else {"
      echo "        Response$n{id: adjusted, body: input.name}"
      echo "    }"
      echo "}"
      echo
    done

    echo "fn main() -> Unit {"
    echo "    print(\"m8 reference corpus\")"
    echo "}"
  } > "$out"

  local lines
  lines="$(awk 'END { print NR }' "$out")"
  echo "generated $out ($lines lines, $handlers handlers)"
}

baseline_field() {
  local metric="$1"
  local field="$2"
  if [ ! -f "$baseline" ]; then
    echo 0
    return
  fi
  awk -v metric="$metric" -v field="$field" '
    BEGIN { value = 0 }
    $0 !~ /^#/ && $1 == metric {
      if (field == "budget") value = $2
      if (field == "baseline") value = $3
    }
    END { print value + 0 }
  ' "$baseline"
}

record_metric() {
  local metric="$1"
  local elapsed="$2"
  local budget
  local previous
  local status="ok"

  budget="$(baseline_field "$metric" budget)"
  previous="$(baseline_field "$metric" baseline)"

  if [ "$budget" -gt 0 ] && [ "$elapsed" -gt "$budget" ]; then
    status="over-budget"
  fi
  if [ "$previous" -gt 0 ]; then
    local limit=$((previous * 105 / 100))
    if [ "$elapsed" -gt "$limit" ]; then
      status="regressed"
    fi
  fi

  printf "%s\t%s\t%s\t%s\t%s\n" "$metric" "$elapsed" "$budget" "$previous" "$status" >> "$metrics"

  if [ "$enforce" = true ] && [ "$status" != "ok" ]; then
    if is_known_gap "$metric"; then
      echo "m8 benchmark: $metric $status (${elapsed}ms, budget ${budget}ms, baseline ${previous}ms) — known gap, not enforced (docs/m8-status.md)" >&2
      return 0
    fi
    echo "m8 benchmark: $metric $status (${elapsed}ms, budget ${budget}ms, baseline ${previous}ms)" >&2
    return 1
  fi
}

measure() {
  local metric="$1"
  shift
  local start
  local end

  start="$(now_ms)"
  "$@"
  end="$(now_ms)"
  record_metric "$metric" "$((end - start))"
}

if [ ! -x "$keelc" ]; then
  cargo build --release -p keelc-driver
fi

generate_corpus
mkdir -p "$(dirname "$metrics")"
printf "metric\telapsed_ms\tbudget_ms\tbaseline_ms\tstatus\n" > "$metrics"

measure keel_check "$keelc" check "$work_dir/main.keel" --milestone M7

if [ "$mode" = "full" ]; then
  measure keel_build_cold "$keelc" build "$work_dir/main.keel" --milestone M7
  measure keel_build_incremental "$keelc" build "$work_dir/main.keel" --milestone M7
fi

cat "$metrics"
